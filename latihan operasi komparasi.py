# operasi komparsi (membandingkan nilai)
# setiap hasil dari komparasi adalah boolean (true/false)
# >,<,>=,<=,==,!=,is,is not

a = 4
b = 2
# lebih dari
print("===Lebih Dari(>)===")
hasil = a > 3
print(a,'>',3,'=' ,hasil)
hasil = b > 3
print(b,'>',3,'=' ,hasil)

# Kurang dari
print("===Kurang Dari(<)===")
hasil = a < 3
print(a,'<',3,'=' ,hasil)
hasil = b < 4
print(b,'<',4,'=' ,hasil)

# Lebih dari sama dengan
print("===Lebih Dari Sama Dengan(>=)===")
hasil = a >= 3
print(a,'>=',3,'=' ,hasil)
hasil = b >= 3
print(b,'>=',3,'=' ,hasil)
