# tipe data1 = angka satuan/tidak ada koma (integer)
data = 16
print(type(data)) # cara melihat tipe data
print("data : ", data, " bertipe : ", type(data))

# tipe data2 = angka dengan koma (float)
data = 1.5
print(type(data)) # cara melihat tipe data
print("data : ", data, " bertipe : ", type(data))

# tipe data3 = kumpulan karakter (string)
data = "Rahma"
print(type(data)) # cara melihat tipe data
print("data : ", data, " bertipe : ", type(data))

# tipe data4 = biner true/false (boolean)
data = True
print(type(data)) # cara melihat tipe data
print("data : ", data, " bertipe : ", type(data))

# tipe data4 = biner true/false (boolean)
data = False
print(type(data)) # cara melihat tipe data
print("data : ", data, " bertipe : ", type(data))

## tipe data khusus
# bilangan complex
data = complex(5,6)
print(type(data)) # cara melihat tipe data
print("data : ", data, " bertipe : ", type(data))

